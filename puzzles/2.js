const outcomeValues = {
    win: 6,
    tie: 3,
    lose: 0
};
let games = input.split('\n');

games = games.map(game => {
    const plays = game.split(' ');
    return {
        opponent: plays[0],
        result: plays[0],
        me: plays[1]
    }
});
const resultsMap = [
    {label: 'lose',
        letter: 'X',},
    {label: 'tie',
        letter: 'Y',},
    {label: 'win',
        letter: 'Z',}
    ];
function getGameScore(opponentLetterVal, resultLetterVal) {
    // console.log('opponentLetterVal', opponentLetterVal);
    // console.log('meLetterVal', meLetterVal);
    const expectedResult = resultsMap.find(result => result.letter === resultLetterVal);
    let myHand;
    const opponentHand = rpsOptions.find((option) => option.opponentLetterVal === opponentLetterVal);

    if(expectedResult.label === 'lose') {
        myHand = rpsOptions.find((option) => option.shapeVal === opponentHand.beats[0]);
    } else if (expectedResult.label === 'win') {
        myHand = rpsOptions.find((option) => option.beats[0] === opponentHand.shapeVal)
    } else {
        myHand = rpsOptions.find(option => option.shapeVal === opponentHand.shapeVal);
    }
    // console.log(expectedResult.label);
    // console.log('myhand', myHand);
    // console.log('opponentHand', opponentHand);

    if(opponentHand.beats.includes(myHand.shapeVal)){
        return outcomeValues.lose + myHand.score;
    } else if(myHand.beats.includes(opponentHand.shapeVal)) {
        return outcomeValues.win + myHand.score;
    } else {
        return outcomeValues.tie + myHand.score;
    }
}
